# tuple
#
# 另一种有序列表叫元组：tuple。tuple和list非常类似，但是tuple一旦初始化就不能修改，比如同样是列出同学的名字：
classmates = ('Michael', 'Bob', 'Tracy')

# list
#
# Python内置的一种数据类型是列表：list。list是一种有序的集合，可以随时添加和删除其中的元素。
#
# 比如，列出班里所有同学的名字，就可以用一个list表示：
classmates = ['Michael', 'Bob', 'Tracy']